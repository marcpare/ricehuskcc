<p>It's been a bit rainy in Seattle lately, and we're afraid it's messing up our testing.</p>

<p>The test results the last five days or so have been quite strange, definitely not according to plan. We've been systematically trying to get at the root cause of the issues, though haven't quite made it yet. Currently, we're investigating moisture content.
</p>
<p>Moisture content of fuels in biomass can have a dramatic effect on the performance of a combustion or gasification device.</p>

<p>Moisture in rice husk slows down the reactions occurring in the device. It takes time and energy to vaporize water that would otherwise go into raising the temperature of the combustion zone.</p>

<p>There are a number of ways to test for moisture content in biomass fuels. Unfortunately, there are no inexpensive off the off-the-shelf instruments capable of measuring the moisture content of rice husk. In a past project, we hoped to DIY a moisture meter (essentially <a href="http://gardenbot.org/howTo/soilMoisture/">rusty nails wired together</a>). But, it never materialized.</p>

<p>A very common method is the oven-dry method. Heat a sample of biomass in an oven at low enough temperature to vaporize the water but not pyrolyze the biomass. Measure the mass before and after. The mass lost is the mass of water in the sample.</p>

<p>I hoped to speed this up today by doing it in the microwave at the 150 C setting.</p>

<p>I wasn't able to find any oven-drying literature on rice husk. I thought perhaps some difficulties would arise from the small particle size of the rusk. Perhaps, the insulated bunch underneath the pile would heat up quickly and pyrolyze. Perhaps the silica structure of the rice hull would require higher temperatures to vaporize the water.</p>

<p>Still no definitive answers to these questions, but I was able to reduce the mass of a rice husk sample by about 10% through successive microwave nukes.</p>


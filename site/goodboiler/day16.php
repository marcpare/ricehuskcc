<p>What do you do once you've got a conceptual design for a cook stove?</p>

<p>The short answer is to try as many design variations as quickly as possible, changing only one thing at a time.</p>

<p>Let me make the case for this approach, as it is not necessarily obvious that this is the right way to go.</p>

<p>For our particular conceptual design, we start with this (incomplete) list of unknowns:</p>


<p>What are some reasonable values for these unknowns? Here are a few off the top of my head:</p>

<ul><li>Height of reactor | 4, 6, 8 cm</li>
<li>Diameter of reactor | 4, 6, 8 cm</li>
<li>Fill height of rice husk | 2, 4, 6, 8 cm</li>
<li>Diameter of inner chimney | 2, 4 cm</li>
<li>Height of outer chimney | 4, 8 cm</li>
</ul>

<p>The total number of combinations is 3x3x4x2x2 = 144.</p>

<p>At this point you might feel a bit of hesitation. Isn't that a lot of work to do?</p>

<p>Indeed, it is a lot of work. </p>

<p>However, there is another point of view that you can take that makes the process enjoyable.</p>

<p>Can you predict the exactly difference in performance between a reactor at 4cm diameter versus 6cm? The larger diameter will have a high power output, but maybe it won't burn well because of increased air flow resistance. It is a mystery what will actually happen. This is great! The more tests we do, the more likely we are to stumble into something surprising that works better than we had thought possible.</p> 

<p>If you look carefully, you can see this is the secret behind many great inventors.</p>

<p>Thomas Edison:</p>
<blockquote>
  <p>"I have not failed. I've just found 10,000 ways that won't work."</p>
</blockquote>

<p>James Dyson:</p>
<blockquote>
  <p>"Dyson developed 5,127 prototype designs between 1979 and 1984."</p>
  <p>&mdash; <em>Against the Odds</em></p>
</blockquote>

<p><a href="http://en.wikipedia.org/wiki/Josiah_Wedgwood">Josiah Wedgwood</a> (the great potter):</p>
<blockquote>
  "...there follows the first of nearly five thousand carefully recorded trials, carried out over the next thirty-five years"
  
  <p>&mdash; <em>The Lunar Men</em>, Jenny Uglow</p>
</blockquote>

<p>A rapid prototyping process allows you to explore many possible designs. In doing so, you are increasing our probability of finding out something unexpected. If you take care to only change one thing at a time, you will reveal the exact cause of differences in design performance. People will tell you that you were lucky to stumble into something so much better, but you'll know that you manufactured the luck yourself.</p>



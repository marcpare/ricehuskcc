<p>The work so far has been organized but not directed at something in particular. I've poked and prodded at different aspects of the problem using prototypes, calculation, research, and writing.</p>

<p>This is a first strike at a solution to the Good Boiler design problem. (introduced on <a href="day1">Day 1</a> and <a href="day2">Day 2</a>)</p>

<p>On <a href="day8">Day 8</a>, I got hugely encouraging results for the heart of the design: clean, easy combustion using natural draft.</p>

<p>Sizing calculations on <a href="day9">Day 9</a> suggested that I can boil water with about the same volume of fuel as water, which is a favorable size for a household product.</p>

<p>This cloud of ideas and results has now come together into a concrete concept. I will test this concept for feasibility. If it passes, I'll refine it into a set of fabrication plans.</p>

<p>It's hard to say exactly what size will be most useful to people. Families may want to boil 7 liters of water at a time for a meal or just 1/2 liter at a time to make a cup of tea. There is probably room for a range of product sizes.</p>

<p>I'll aim at getting the 1/2 Liter size working, since it's easier and cheaper to build the small size. If we can pull it off, it will be a proof of concept that motivates work by me or others to scale up the device.</p>

<p>Remember, this effort is about widespread collaboration, not high-ego design by an individual.</p>

<p>Up next is building a boiling test rig to see how much water can be boiled in order to verify my sizing calculation.</p>

<p>I will also break down the combustion tests into a more rigorous exploration of the design space.</p>


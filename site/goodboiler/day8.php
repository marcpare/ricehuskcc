<p>The rice husk has arrived. The test results using real fuel are awesome and possibly groundbreaking.</p>

<p>We nearly didn't get to run the tests.</p>

<p>We were planning to do the tests in a fire pit used for bonfires. We were going to do it on a property that regularly burns large piles of agricultural waste. 
</p>

<p>From pictures posted so far, you can see that the tests being doing are no more dangerous than lighting a charcoal grill. The smoke produced from these testing units is so little that it disperses into the air a few feet from the testing location. It's not a reasonable safety, fire, or environmental hazard.
</p>

<p>Unfortunately, the property owner, when faced with something unknown, retreated into the comfort of "safety first".</p>

<p>Just the mention of fire conjures up images of Hollywood explosions and raging forest fires. Never mind that the explosive limits for biomass gasification are not possible to reach without specialized equipment.</p>

<p>Do you know how your boiler works? Do you know how your furnace works? Or do you just trust that they are safe because the contractor told you it was ok?</p>

<p>Unfortunately, to a person that has holed up in the shell of "safety", no amount of reasoning can counteract the negative imagination.</p>

<p>Mike Rowe, the host of Discovery Channel's Dirty Jobs, came to a similar conclusion as a result of his experience with the men and women that make this country work.</p>

<blockquote>
  
<p>You want to hear the dirty truth? Safety is not really ever first.</p>


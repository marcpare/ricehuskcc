<p>This has the advantage that we can take the rig and test most anywhere. In the driveway, at the neighbors, or even at the beach.
</p>

<p>This gives us the opportunity to run into people while testing and let them know what we're up to. Testing outside at Georgia Tech introduced our team to people working on stoves on six continents. We got to meet junkyard dealers, tin roofers, the fire department, crazy old men, and distinguished professors.</p>

<p>It's tempting as an engineering student to feel that you have to jump straight to a high precision experimental rig to test your idea. The engineering education, while broadening your modeling ability, makes it easy to doubt that value of simple, exploratory testing because you are aware of the many unknowns in any physical situation.
</p>

<p>Bridging the gap between tin can prototypes and engineering analysis isn't easy. You can spend years tinkering with hole sizes in a tin can without making meaningful progress. You do need to know when to quit and invest in the real equipment.</p>

<p>On the other hand, you can build a high precision particulate sampling apparatus that tells you nothing more than "traditional fires produce a lot of smoke." Or, "when you build a stove exactly this way, it's usually better to use 5 cm chunks of wood instead of 4 cm chunks."</p>

<p>In the early stages of a project, you're looking for concrete results to share and spark bigger investments in time and energy. For that, Obtainium is a perfect fit. You can test many things very cheaply and very quickly. You're not trying to save the world with a tin can, just to provide a provocative spark.</p>

<p>Here's a list of materials and tools that are useful for prototyping. These materials are low cost, easy to fabricate, and/or are useful for stove building.</p>


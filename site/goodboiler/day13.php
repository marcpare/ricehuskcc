
<p>Don't throw that can away!</p>

<p>Why not?</p>

<p>It's <em>Obtainium</em></p>

<p>Obtainium &mdash; <em>n.</em> ob·tain·i·um &mdash; the usually unperceived materials all around that can be repurposed into sculpture, crafts, and science experiments. Examples of Obtainium include tin cans and soda bottles. Derived from it's opposite <a href="http://en.wikipedia.org/wiki/Unobtainium">Unobtainium</a>.</p>

<p>I can't even begin to say enough how much I love prototyping with Obtainium.</p>

<p>Obtainium for stove design is tin cans, soda bottles, pine cones, duct work, hardware cloth, and more. I put a list at the bottom of useful materials and tools for the Obtainium prototyping stage.</p>

<p>This week, I improved my prototyping setup a bit, managing to fit my upcoming series of experiments in two coffee tins.</p>


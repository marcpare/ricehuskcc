
<p>The inspiration for this week's day of rest is the American Benjamin Franklin.</p>

<p>Ben Franklin, the troublemaker that convinced us all that "early to bed, early to rise makes a man healthy, wealthy and wise," was actually quite fond of leisure time.</p>

<blockquote>
<p>During one of Franklin’s late-night chess matches in Passy, a messenger arrived with an important set of dispatches from America. Franklin waved him off until the game was finished.
</p>
<p>Another time, he was playing with his equal, the Duchess of Bourbon, who made a move that inadvertently exposed her king. Ignoring the rules of the game, he promptly captured it. “Ah,” said the duchess, “we do not take Kings so.” Replied Franklin in a famous quip: “We do in America.” </p>

<p>One night in Passy he was absorbed in a game when the candles flickered out. Refusing to quit, he sent his opponent to find more. The man quickly returned with a surprised look and the news that it was already light outside. Franklin threw open the shutters. “You are right, it is daytime,” he said. “Let’s go to bed.” </p>

<p>&mdash;Isaacson, Walter. Benjamin Franklin.</p> 
</blockquote>



<p>In reading about Franklin's life and work, it's easy to see how he channeled an attitude of leisure and play into stunning achievement. The "electric patriot" tinkered with his stove, measured ocean temperatures with a glass bottle and string, and, of course, invented the lightning rod.</p>

<p>It's hard to imagine doing these sorts of things in a directed way, according to some plan. Many of Franklin's achievements seem to come from those days when you're sitting around, wondering what to do, and you think "I wonder what would happen if..."</p>


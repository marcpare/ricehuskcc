<h2>Day 4: Rapid Prototyping</h2>
<h3 class="date">August 17, 2013</h3>

<p>I have a confession: I have have been prototyping designs since Day 1.
</p>
<p>I promised to detail some difficulties with Cylindrical Gasifier design today, but I think it's more important to share the reasoning behind such early prototyping before getting on to the promised topic.</p>

<p>There are so many variables to consider in the design of a stove that it's too difficult to predict on paper what will happen with a particular design.
</p>
<p>Even with the world's most powerful computers, engineer's can't solve the Navier-Stokes equations (the equations of fluid mechanics, which describe exactly how a fluid or gas behaves). Further increasing the difficulty of stove design is that the gas behavior is coupled to the heat transfer and combustion chemistry taking place.
</p>
<p>But you can solve the Navier-Stokes equations at your desk right now. Pour a glass of water onto the table. The way the water flows is the solution to the equation. It doesn't matter what the calculations say, you know the answer now. This is the power of prototyping.
</p>
<p>Do you still doubt that backyard prototyping can contribute something meaningful to a design process? Here's what Dr. Tom Reed, an MIT Chemical Engineer has to say,
</p>
<blockquote>
We built and operated a 1 ton/day gasifier at the Solar Energy Research
Institute, spending possibly $6 million for designers, fabricators,
instrumentation engineers and rented space at Hazen Research here in Golden.
Each institutional step could take days or weeks to complete.

While waiting for the institutional wheels to grind, we took matters into our
own hands and built a 5 gallon pail gasifier in the alley behind our laboratory.
It had a blower and a flair, and we learned more about the basic science of
gasification in that zero cost project than we learned from the $6 million
project.
</blockquote>

<p>Prototyping techniques for improved stoves deserves more than just one post. There are experimental techniques to use. Materials to consider. Engineering to leverage. Mindsets to adopt. As this design process goes, I hope to get to share the range of techniques I've learned from university professors, picked up from rural mechanics, and developed on my own.
</p>
<p>Today, here are my notes from the two burn trials I have done so far as illustration of the techniques.
</p>
<hr>

<p>Aug 16 Trials.
</p>
<p>Primary challenge to rice husk stoves is getting clean, quick lighting. Can a wire mesh cage with central air column achieve quick lighting?
</p>
<p>Will prototype using wood chips first. If I can't get wood chips to light, then I definitely won't be able to get rice husk to light. 
</p>
<p>Secondary goal is to observe flame and smoke behavior. A very smokey combustor with wood chips is not acceptable, as it will be worse when switching to rice husk.
</p>
<p>(1) Bottom Lit Wood Chip Cage
</p>
<p>Lights easily. Flame doesn't channel up into the central column. Needs a wind screen.
</p>
<p>(2) Top Lit Wood Chip Cage
</p>
<p>Lights easily. Wind screen really helps. Relatively smoke-free, but can be improved.
</p>
<p>Conclusions:
</p>

<ul>
<li>Lighting is a success</li>
<li>Increase size of central column to try to contain flames</li>
<li>Decrease gap between wind screen and outer mesh to discourage flames on the outside, as these cause the most smoke
</li>
</ul>

<hr>

<p>It takes a certain amount of skill to prototype well. You need to be able to break down a big idea into a small, testable part. 
</p>

<p>But I think the hard part of prototyping has more to do with your ego than anything else. It's a modest exercise to cut some tin cans and set some twigs on fire. It's easier to assume your idea will work than it is to try to tear it apart via experiment. You never "become a good designer", you must choose each time you embark on a design exercise to approach your gaps in knowledge with humility and discipline.
</p>









<p>The most recent innovation in devices that cook using rice husk as fuel is Dr. Belonio's Cylindrical Gasifier. It demonstrates a beautiful blue flame consistently. It has very simple construction. <a href="http://www.bioenergylists.org/stovesdoc/Belonio/Belonio_gasifier.pdf">Dr. Belonio's Handbook</a>, a gem of documentation, describes in great detail the many facets of its operation.</p>

<p>Dr. Belonio's pioneering work now allows us to consider new design territory for exploration.</p>

<p>The possible areas of improvement to the Cylindrical Gasifier can make up many posts. I want to focus on one today: repeatability of burner performance.</p>

<p>The Cylindrical Gasifier produces a gas that is burned in a many-holed diffusion burner.</p>

<p class="centered">

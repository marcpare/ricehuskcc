<p>
The small change in gap size influenced the air flow such that the amount of air entrained was different. The change in air amount dramatically affects Overall Thermal Efficiency (you can even get negative efficiency, cooling down the cooking pot, with enough air).</p>

<p>It might seem like a nitpick to point out this difference in behavior until you consider the realities of stove construction and dissemination in developing countries.
</p>
<p>How easy it is to build a stove in rural Cambodia and get the size of a cylinder to within 1cm of the design? Have you ever tried to bend and roll cylinders by hand?
</p>
<p>What if the design is being passed along by word of mouth?
</p>
<p>How can you tell that the stove is performing at high efficiency? Is there time to do testing of every stove? What if the stove performance changes over time as the materials wear out and deform?
</p>
<p>These are big questions. Each project will have a different set of answers.
</p>
<p>I think the implication for designers and engineers is that we attempt to quantify and communicate the variability in performance of our designs.
</p>

<p>

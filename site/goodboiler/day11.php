</p>
<p class="caption">Diffusion burner on a Cylindrical Gasifier</p>

<p>Since there is no air available by the time the pyrolysis gas reaches the top of the burner, air is supplied above the burner, from the ambient environment.</p>

<p>The air is entrained into the gas stream according to how fast the pyrolysis gas is exiting the reactor. There is not a significant amount of turbulence, so the flame takes on the characteristic tear-drop shape of a diffusion flame.</p>

Depending on how much air is entrained, I have seen the Overall Thermal Efficiency of the stove range from 20% to 35%.

This wouldn't be so bad if we had good control over the air entrainment. Unfortunately, it is very difficult to control.


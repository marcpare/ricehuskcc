<p>It's great if our high performance design works in the lab, but what good will the design actually do for a person working in the field?
</p>
<p>Thinking about it another way, what technology would a project be more likely to choose: one that is flexible and reliable under a variety of conditions or a high-performance device that has only seen laboratory-grade fuel?
</p>














